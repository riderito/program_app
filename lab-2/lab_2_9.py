def count_word():
    """
    Функция для подсчета количества точных вхождений слова в строку.
    Учитывает только целые слова, игнорируя вхождения внутри других слов.

    Запрашивает у пользователя:
    1. Исходную строку (s)
    2. Слово для поиска (m)
    Выводит количество вхождений слова m в строку s.
    """

    print("Задача 2.9: Количество слов m в строке s.")

    # Ввод данных
    text = input("Введите строку s: ")              # Получаем строку для поиска
    word = input("Введите слово m для поиска: ")    # Получаем искомое слово

    # Инициализация параметров
    count = 0                       # Счетчик вхождений
    word_len = len(word)            # Длина искомого слова
    text_len = len(text)            # Длина строки
    word_boundaries = " .,!?;:()"   # Допустимые границы слов

    # Основной цикл поиска вхождений
    # Проходим по всем возможным начальным позициям (i), с которых слово может начинаться в строке
    for i in range(text_len - word_len + 1):
        # Проверка совпадения подстроки длиной word_len, начиная с позиции i, с искомым словом
        if text[i:i + word_len] == word:
            # Слева от слова либо начало строки (i == 0), либо не буква
            left_ok = (i == 0) or (text[i - 1] in word_boundaries)
            # Справа либо конец строки, либо не буква
            right_ok = ((i + word_len) == text_len) or (text[i + word_len] in word_boundaries)

            # Если слово имеет правильные границы
            if left_ok and right_ok:
                count += 1

    print(f"Количество вхождений слова '{word}': {count}")


if __name__ == "__main__":
    count_word()