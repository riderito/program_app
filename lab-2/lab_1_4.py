def process_sequence():
    """
    Запрашивает у пользователя последовательность чисел, вычисляет их сумму и количество.
    """
    print("Задача 1.4: Сумма и количество чисел в последовательности.")
    sequence = input("Введите последовательность целых чисел через пробел: ")

    total_sum = 0   # Сумма чисел
    count = 0       # Количество чисел
    start = 0       # Указатель на начало текущего числа в строке
    index = 0       # Индекс в строке

    # Проходим по всей строке + 1 символ для обработки последнего числа
    while index <= len(sequence):
        # Условие конца числа (пробел или конец строки)
        if index == len(sequence) or sequence[index] == " ":
            # Если между start (индекс начала числа) и index (текущий индекс) есть цифры
            if start != index:
                # Преобразуем и добавляем к сумме
                total_sum += int(sequence[start:index])
                # Увеличиваем счётчик
                count += 1
            # Обновляем указатель на начало следующего числа
            start = index + 1
        # Переход к следующему символу
        index += 1

    print(f"Сумма чисел: {total_sum}")
    print(f"Количество чисел: {count}")


if __name__ == "__main__":
    process_sequence()