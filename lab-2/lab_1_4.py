def process_sequence():
    """
    Запрашивает у пользователя последовательность чисел, вычисляет их сумму и количество.
    """
    print("Задача 1.4: Сумма и количество чисел в последовательности.")
    sequence = input("Введите последовательность целых чисел через пробел: ")

    # Инициализация пустого списка для хранения чисел
    numbers = []
    # Указатель на начало текущего числа в строке
    start = 0

    # Разбор строки на числа

    # Цикл действует столько раз, сколько символов в sequence
    for i in range(len(sequence)):
        # Если встретили пробел - значит, текущее число закончилось
        if sequence[i] == " ":
            # Проверяем, что между start и i (номер текущего символа) есть символы
            if start != i:
                # Преобразуем подстроку в число и добавляем в список
                numbers.append(int(sequence[start:i]))
            # Перемещаем указатель на начало следующего числа
            start = i + 1

    # Добавляем последнее число в строке (если оно есть)
    if start < len(sequence):
        numbers.append(int(sequence[start:]))

    # Проверяем, что список чисел не пустой
    if not numbers:
        print("Последовательность пуста.")
        return # Завершаем выполнение функции

    # Инициализация переменных для подсчета суммы и количества
    total_sum = 0   # Общая сумма чисел
    count = 0       # Количество чисел
    index = 0       # Текущий индекс в списке numbers

    # Вычисление суммы и количества с использованием цикла while
    while index < len(numbers):
        # Добавляем текущее число к общей сумме
        total_sum += numbers[index]
        # Увеличиваем счетчик чисел
        count += 1
        # Переходим к следующему числу
        index += 1

    print(f"Сумма чисел: {total_sum}")
    print(f"Количество чисел: {count}")


if __name__ == "__main__":
    process_sequence()